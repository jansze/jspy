# Modulo operator %

# Operator is just a symbol that has a specific function

# 10 % 5 = 2 // 5 goes into 10 twice with no remainder.

# Simple logic to check if a number is even or odd.

input = int(input("Please choose a random number: "))
check = input % 2

if check == 0:
    print("The number is even.")
else:
    print("The number is odd.")
